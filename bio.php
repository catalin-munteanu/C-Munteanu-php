            <p>Catalín Munteanu es el proyecto solista de Catalina Pampin, compositora, cantante e instrumentista de Buenos Aires, Argentina.
            </p>

            <p>Actualmente se encuentra presentando su último single, <a href="https://open.spotify.com/album/4jYva9PDnECQQnJuMywXG2?si=OiTtxeQpSUGmhlxXPWxRlw" target=_blank>Al sur</a> (2022), en el que encabeza un power trío junto a Facundo Semerena (Hungría, FKRZ) en batería y Germán Hrabina (Hormigas) en bajo, dando forma a una propuesta sonora renovada y explosiva.
            </p>

            <p>Su música se caracteriza por la experimentación sonora con ritmos irregulares, hipnóticos loops en vivo con distintos instrumentos y una búsqueda permanente de expresiones y matices vocales. Las composiciones tienen una fuerte impronta poética, con textos incisivos y sensibles que reflejan una particular observación del mundo.
            </p>

            <p>Editó dos discos solistas de forma independiente, <a href="https://open.spotify.com/album/31t5YnjAt782oUyU5lhTUI?si=You3pV13SoGilbm7NvIE0A" target=_blank>Las
