                su ciudad natal y en otros puntos del país.
            </p>

            <p>Dueña de una gran versatilidad artística, participó también en otros proyectos, como <a href="https://nicolasycatalin.bandcamp.com/" target=_blank>Nicolás y Catalina</a>, <a href="https://montarosa.bandcamp.com/" target=_blank>Montarosa</a>, <a href="https://open.spotify.com/artist/0t3u2FzPzoMAxFYeedbkEu" target=_blank>Tuti Posse</a> y El Guitarrazo, con quienes realizó numerosos conciertos y grabaciones; entre ellas, los discos <a href="https://open.spotify.com/album/05qTM0VRUdTV8Bw3yMj8K6?si=s65I2imNRPanaLt4f1_G_A" target=_blank>
